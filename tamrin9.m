% تمرین 3-2

x = [19,3;17,2;18,3;20,1];

% ترانماده کردن ماتریس ایکس
y = x';

% ضرب مقادیر سطر اول ماتری در سطر دوم
a = prod(y);

% استخراج مقادیر واحد ها از ماتریس
v = [x(:,2)];


% محاسبه مجموع عناصر آرایه
z = sum(v);

% محاسبه معدل ترم
b = sum(a)/z;

% مایش نتایج با پیغام مناسب
fprintf('تعداد کل واحد‌ها: %d\n', z);
fprintf('معدل ترم: %.2f\n', b);



