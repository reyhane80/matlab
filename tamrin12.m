% تمرین 6-2 جلسه چهارم

a = [16 15 17;12 11 20;17 18 20;18 19 11;10 9 5;2 4 20];

% تبدیل ماتریس به بردار
b = a(:);

% بررسی شروط
b(b < 5) = 9;
b(b >= 5 & b < 8) = 9.5;
b(b >= 8 & b < 10) = 10;
b(b >= 10 & b < 15) = b(b >= 10 & b < 15) + 1;
b(b >= 15 & b < 20) = b(b >= 15 & b < 20) + 0.5;

% تبدیل بردار به ماتریس 
b = reshape(b, size(a));


disp('نمرات دانشجویان:');
disp(b);
